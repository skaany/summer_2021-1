package day12loops;

public class WhileLoop01 {

	public static void main(String[] args) {
		
		/*
		 print "hi" 4 times on the console
		 */
		
		for(int i = 1; i<5; i++) {
			System.out.println("hi");
		}
		System.out.println("*********");
		
		int i = 1;
		
		
		while(i<5) {
			
			
			System.out.println("hi");
			
			i++;
		}
		

	}

}
