public class OrderOfOperations01 {

	public static void main(String[] args) {
		
		/*
		 Order of operations
		 1- ()
		 2- Exponents
		 3- Do multiplication and division from left to right
		 4- Do addition and subtsrcation from left to right 
		 
		 
		 */
		
		//   38 / 2 - (4 + 3) * 2= ?
		System.out.println(38 / 2 - (4 + 3) * 2);
		
		
		//    8+2*(14-6/2)-12 =?
		System.out.println( 8+2*(14-6/2)-12);
		
		
		
		
		
		
		
		
		
		
		

	}

}
